int		ft_antidote(int i, int j, int k)
{
	int		temp;

	if (i > j)
	{
		temp = i;
		i = j;
		j = temp;
	}
	if (i > k)
	{
		temp = i;
		i = k;
		k = temp;
	}
	if (j > k)
	{
		temp = j;
		j = k;
		k = temp;
	}
	return (j);
}
